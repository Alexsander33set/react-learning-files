console.clear();


//Funções Anonimas
// Para chamar ->  () => {}

let somar = (a, b) =>{
  let total = a + b;
  return console.log(` Somando ${a} e ${b}, o resultado é: ${total}`)
}
somar(10, 30)

let subitrair = (a, b) =>{
  let total = a - b;
  return console.log(` Subtraindo ${a} e ${b}, o resultado é: ${total}`)
}
subitrair(10, 30)


